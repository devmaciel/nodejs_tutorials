// Criar Package JSON usando 'npm init'

/*
* npm init
* ---package name: geralmente nome da pasta principal
* ---version: versão do package
* ---description: descrição do projeto, etc.
* ---entry point: A starter file da aplicação, exemplo 'app.js' 'index.js'
* ---test command: (ignorou)
* ---git repository: git url
* ---keywords: palavras chaves do projeto
* ---author: autor do projeto, pessoa/empresa
* ---license: licensa do projeto, exemplo 'MIT' 'ISC'
* 
* então com isso cria o package.json com essas configs iniciais.
*/